export type TKey = keyof (typeof dictionaries)['ru']

const I18nContext = createContext<{
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: TKey) => string
}>({ lang: 'ru', setLang: () => {}, t: (k) => k })

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('ru')

  useEffect(() => {
    const saved = window.localStorage.getItem('lms_lang')
    if (saved === 'kz' || saved === 'ru') setLangState(saved)
  }, [])

  const setLang = (l: Lang) => {
    setLangState(l)
    window.localStorage.setItem('lms_lang', l)
  }

  const t = (key: TKey) => dictionaries[lang][key] ?? dictionaries.ru[key] ?? key

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>
}

export function useI18n() {
  return useContext(I18nContext)
}
